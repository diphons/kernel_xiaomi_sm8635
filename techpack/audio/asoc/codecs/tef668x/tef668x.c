static int tef668x_start_init(struct tef668x_data *tef668x)
{
	int ret = -1;

	dev_info(tef668x->dev, "tef668x_start_init enter\n");

	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab0);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab0 error\n");
		return ret;
	}
	usleep_range(TEF668x_TIMER_POWER, TEF668x_TIMER_POWER + 100);	//every + 10 ms.

	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab5);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab5 error\n");
		return ret;
	}
	usleep_range(TEF668x_TIMER_POWER, TEF668x_TIMER_POWER + 100);	//every + 10 ms.
	ret = tef668x_boot_state(tef668x);
	if (ret > 0) {
		DevTEF668x_State = eDevTEF668x_Idle_state;
	} else {
		dev_err(tef668x->dev, "boot state error\n");
		return ret;
	}
	usleep_range(TEF668x_TIMER_WAIT_IDLE, TEF668x_TIMER_WAIT_IDLE + 100); //50 ms

//APPL_Get_Operation_Status [ w 40 80 01 [ r 0001 ]
	ret = DevTEF668x_Idle_state(tef668x);
	if (ret > 0) {
		DevTEF668x_State = eDevTEF668x_Wait_Active;

	} else {
		dev_err(tef668x->dev, "DevTEF668x_Idle_state error\n");
		return ret;
	}

	usleep_range(TEF668x_TIMER_WAIT_IDLE, TEF668x_TIMER_WAIT_IDLE + 100); //50 ms
	ret = DevTEF668x_Wait_Active(tef668x);
	if (ret > 0) {
		DevTEF668x_State = eDevTEF668x_Active_state;
	} else {
		dev_err(tef668x->dev, "DevTEF668x_Wait_Active error\n");
		return ret;
	}

	DevTEF668x_Active_state(tef668x);

	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab6);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab6 error\n");
		return ret;
	}

	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab7);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab7 error\n");
		return ret;
	}
	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab8);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab8 error\n");
		return ret;
	}


	return 0;
}

