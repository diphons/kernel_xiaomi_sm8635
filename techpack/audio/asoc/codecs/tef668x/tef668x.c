static bool tef668x_addr_is_in_range(unsigned int addr,
	unsigned int addr_min, unsigned int addr_max)
{
	if ((addr >= addr_min)
			&& (addr <= addr_max))
		return true;
	else
		return false;
}

static bool tef668x_volatile_register(struct device *dev, unsigned int reg)
{
	/* coeff registers */
	if (tef668x_addr_is_in_range(reg, TEF668x_REG_START,
		TEF668x_REG_MAX))
		return true;

	/* control registers */
	switch (reg) {
	case TEF668x_REG_START:
	case TEF668x_REG_INIT:
	case TEF668x_REG_CONTROL:
	case TEF668x_REG_FM_TUNE_TO:
	case TEF668x_REG_AM_TUNE_TO:
	case TEF668x_REG_AUDIO_SET_MUTE:
	case TEF668x_REG_MODULE_INIT:
	case TEF668x_REG_GET_PROPERTY:
		return true;
	default:
		return false;
	}
}

static bool tef668x_writeable_registers(struct device *dev, unsigned int reg)
{
	if (tef668x_addr_is_in_range(reg, TEF668x_REG_START,
		TEF668x_REG_GET_PROPERTY)
		|| tef668x_addr_is_in_range(reg, TEF668x_REG_START,
		TEF668x_REG_MAX))
		return true;
	else
		return false;
}

static bool tef668x_readable_registers(struct device *dev, unsigned int reg)
{
	if (tef668x_addr_is_in_range(reg, TEF668x_REG_START,
		TEF668x_REG_GET_PROPERTY)
		|| tef668x_addr_is_in_range(reg, TEF668x_REG_START,
		TEF668x_REG_MAX))
		return true;
	else
		return false;
}

