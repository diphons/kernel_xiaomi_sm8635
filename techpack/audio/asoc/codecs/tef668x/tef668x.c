static int tef668x_patch_init(struct tef668x_data *tef668x) {
	int ret = -1;
	int i = 0;

	dev_info(tef668x->dev, "tef668x_patch_init enter\n");
//[ w 1C 0000 ]
	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab1);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab1 error\n");
		return ret;
	}
//[ w 1C 0074 ]
	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab2);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab2 error\n");
		return ret;
	}
//pPatchBytes use PATCH.c
	dev_info(tef668x->dev, "tef668x_patch_load pPatchBytes\n");
	ret = tef668x_patch_load(tef668x, pPatchBytes, PatchSize);
	if (ret < 0) {
		while (i < TEF_I2C_RETRIES) {
			dev_err(tef668x->dev, "int pPatchBytes error,wait 10ms,retry five\n");
			usleep_range(TEF668x_TIMER_POWER, TEF668x_TIMER_POWER + 100);	//every + 10 ms.
			ret = tef668x_patch_load(tef668x, pPatchBytes, PatchSize);
			if (ret < 0) {
				dev_err(tef668x->dev, "int pPatchBytes error\n");
				i++;
				if ( i == 5)
					return ret;
			} else
				break;
		}
	}
//[ w 1C 0000 ]
	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab1);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab1 error\n");
		return ret;
	}
//[ w 1C 0075 ]
	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab3);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab3 error\n");
		return ret;
	}
//pPatchBytes use PATCH.c
	dev_info(tef668x->dev, "tef668x_patch_load  pLutBytes \n");
	ret = tef668x_patch_load(tef668x, pLutBytes, LutSize);
	if (ret < 0) {
		dev_err(tef668x->dev, "int pLutBytes error,wati 10ms,retry again\n");
		usleep_range(TEF668x_TIMER_POWER, TEF668x_TIMER_POWER + 100);	//every + 10 ms.
		ret = tef668x_patch_load(tef668x, pLutBytes, LutSize);
		if (ret < 0) {
			dev_err(tef668x->dev, "int pLutBytes two error\n");
			return ret;
		}
	}
//[ w 1C 0000 ]
	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab1);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab1 error\n");
		return ret;
	}
//[ w 14 0001 ]
	ret = tef668x_i2c_writes(tef668x, devTEF668x_Patch_CmdTab4);
	if (ret < 0) {
		dev_err(tef668x->dev, "int tab error\n");
		return ret;
	}
	dev_info(tef668x->dev, "tef668x_patch_init leave\n");
	return ret;
}

